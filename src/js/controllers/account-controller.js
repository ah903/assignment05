///////////////////////////////////////////////////////////////////////////////////////////
// Account Controller
///////////////////////////////////////////////////////////////////////////////////////////
// Manages the data that is presented to the view and provides interaction points
// To Back end service functionality. Attached to attire-app module. Uses Minification
// Safe Injection mechanism
///////////////////////////////////////////////////////////////////////////////////////////
// Dependencies
// $scope
///////////////////////////////////////////////////////////////////////////////////////////
angular.module("attire-app").controller("AccountController", ["$scope","$rootScope", "OrderFactory", function($scope,$rootScope,OrderFactory){
   
    ///////////////////////////////////////////////////////////////////////////
    // Initial View - Load Data When Controller Is Instantiated
    ///////////////////////////////////////////////////////////////////////////
    OrderFactory.getOrdersForUser($rootScope.CurrentUser,function(response){
        $scope.PreviousOrders = response;     
    });

}]);